package com.example.sis_pikmobile.model;

public class VerifikasiModel {
    String id, permohonan_id, gambar, keterangan;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPermohonan_id() {
        return permohonan_id;
    }

    public void setPermohonan_id(String permohonan_id) {
        this.permohonan_id = permohonan_id;
    }

    public String getGambar() {
        return gambar;
    }

    public void setGambar(String gambar) {
        this.gambar = gambar;
    }

    public String getKeterangan() {
        return keterangan;
    }

    public void setKeterangan(String keterangan) {
        this.keterangan = keterangan;
    }
}
